        <h1>About BIONICLE</h1>
        <p>“Since it began in 2001, the BIONICLE universe has grown from being a construction toyline with a story behind it to a modern mythology. Filled with amazing locations, interesting characters, mystery, drama, and action, it has inspired movies, novels, [and] web fiction.” — Greg Farshtey</p>
        <h1>About Wall of History</h1>
        <p>Wall of History is a web archive of the entire BIONICLE legend, created by a fan who wanted to provide others with an accessible way to experience it. The website is still a work in progress. If you have any comments or suggestions, please contact us through the contact page.</p>
        <p>Wall of History has official <a href="https://www.facebook.com/WallofHistory/">Facebook</a>, <a href="https://www.instagram.com/wall_of_history/">Instagram</a>, and <a href="https://twitter.com/Wall_of_History">Twitter</a> accounts, which we would encourage you to follow.</p>
        <h2>Our History</h2>
        <p>Inspired by multimedia web comics like <em>Homestuck</em>, the earliest version of Wall of History began development on October 1<span class="superscript">st</span>, 2017, with a fan transcription of <em>BIONICLE Chronicles #1: Tale of the Toa</em>. The earliest build of the website went live on February 23<span class="superscript">rd</span>, 2019, at about 7:00 AM UTC, and regular content updates continued until July of that same year.</p>
